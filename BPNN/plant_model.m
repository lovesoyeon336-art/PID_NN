function y = plant_model(u, ts)
% =========================================================================
% 非线性时变被控对象
% 模型：y(k) = a(k)/(1+y²(k-1)) * y(k-1) + u(k-1)
%       其中 a(k) = 1.2 * (1 - 0.8*exp(-0.1*k))
% =========================================================================

persistent y_1 u_1 k

if isempty(y_1)
    y_1 = 0;
    u_1 = 0;
    k   = 0;
end

k = k + 1;

% 时变系数
a_k = 1.2 * (1 - 0.8 * exp(-0.1 * k));

% 计算当前输出
y = a_k / (1 + y_1^2) * y_1 + u_1;

% 更新状态
u_1 = u;
y_1 = y;
end
