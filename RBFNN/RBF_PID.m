function [u, kp, ki, kd] = RBF_PID(rin, yout)
% =========================================================
%  RBF 神经网络 PID 参数自整定控制器
%  适用于 Simulink 中的 MATLAB Function 模块
% ---------------------------------------------------------
%  输入端口（Simulink连线顺序）：
%    rin   — 设定值（参考输入，标量）
%    yout  — 被控量（对象实际输出，标量）
%
%  输出端口：
%    u     — PID 控制量（送往被控对象）
%    kp    — 当前比例系数（可接 Scope 观察整定过程）
%    ki    — 当前积分系数
%    kd    — 当前微分系数
%
%  算法结构：
%    ┌─────────────────────────────────────────────────┐
%    │  增量式 PID                                      │
%    │  Δu = Kp·x1 + Ki·x2 + Kd·x3                    │
%    │  x1 = Δe, x2 = e, x3 = Δ²e                     │
%    ├─────────────────────────────────────────────────┤
%    │  RBF 网络（在线辨识 ∂y/∂u）                      │
%    │  输入：[x1, x2, x3]                              │
%    │  隐层：m 个高斯核，固定中心 c 和宽度 b            │
%    │  输出：w'·h  ≈  ∂y/∂u（Jacobian 估计）           │
%    │  权重更新：LMS（辨识误差驱动）                    │
%    ├─────────────────────────────────────────────────┤
%    │  PID 参数更新（梯度下降 + 动量）                  │
%    │  ∂E/∂Kp = −e·(∂y/∂u)·x1，其余类同               │
%    └─────────────────────────────────────────────────┘
% =========================================================

% --------------------------------------------------------
%  【第一区】超参数——所有可调量集中在此处
% --------------------------------------------------------
n_hidden  = 6;       % 隐层高斯核个数（建议范围：4~12）

% RBF 网络固定结构参数（中心和宽度不在线更新，保证稳定性）
b_init    = 3.0;     % 高斯核宽度初值（统一值，可按需改为向量）
c_spread  = 1.5;     % 中心分布范围：每维均匀分布在 [-c_spread, c_spread]

% 学习率（建议先用保守值，若收敛慢再适当加大）
eta_w     = 0.10;    % 输出权重学习率（RBF 辨识）
eta_kp    = 0.05;    % Kp 梯度步长
eta_ki    = 0.05;    % Ki 梯度步长
eta_kd    = 0.05;    % Kd 梯度步长
momentum  = 0.05;    % 动量系数（抑制 Kp/Ki/Kd 的振荡）

% PID 参数初值
Kp_init   = 0.10;
Ki_init   = 0.01;
Kd_init   = 0.01;

% PID 参数限幅（防止参数发散或出现负增益）
Kp_max    = 10.0;   Kp_min = 0.001;
Ki_max    =  5.0;   Ki_min = 0.000;
Kd_max    =  5.0;   Kd_min = 0.000;

% 控制量限幅（匹配实际执行器范围）
u_max     =  10.0;
u_min     = -10.0;

% Jacobian 下限（防止梯度消失导致 PID 参数冻结）
dydU_min  = 1e-4;

% --------------------------------------------------------
%  【第二区】Persistent 状态变量声明
% --------------------------------------------------------
persistent w            % 输出层权重        [n_hidden × 1]
persistent b            % 高斯核宽度         [n_hidden × 1]
persistent c            % 高斯核中心         [n_hidden × n_input]
persistent Kp Ki Kd     % 当前 PID 参数
persistent Kp_prev Ki_prev Kd_prev   % 上一步 PID（动量计算用）
persistent e_1 e_2      % e(k-1), e(k-2)
persistent u_1          % u(k-1)
persistent h_1          % h(k-1)：上一步隐层输出（权重更新用）
persistent y_hat        % ŷ(k)：上一步对网络输出的预测（辨识误差用）

% --------------------------------------------------------
%  【第三区】首步初始化（persistent 为空时执行一次）
% --------------------------------------------------------
if isempty(w)
    n_input = 3;    % 固定：与 [x1, x2, x3] 对应

    % 输出层权重：小随机值（固定种子，保证仿真可复现）
    rng(0);
    w = 0.50 * (rand(n_hidden, 1) - 0.5);

    % 宽度：统一初值
    b = b_init * ones(n_hidden, 1);

    % 中心：每维在 [-c_spread, c_spread] 均匀分布
    c_1d = linspace(-c_spread, c_spread, n_hidden)';   % [n_hidden × 1]
    c    = repmat(c_1d, 1, n_input);                   % [n_hidden × 3]

    % PID 初值
    Kp = Kp_init;  Ki = Ki_init;  Kd = Kd_init;
    Kp_prev = Kp;  Ki_prev = Ki;  Kd_prev = Kd;

    % 历史量清零
    e_1   = 0;  e_2 = 0;
    u_1   = 0;
    h_1   = zeros(n_hidden, 1);
    y_hat = 0;
end

% ========================================================
%  【主循环】以下代码每个仿真步长执行一次
% ========================================================

% --------------------------------------------------------
%  Step 1：计算误差及增量式 PID 三个输入分量
% --------------------------------------------------------
e  = rin - yout;           % 当前误差
x1 = e - e_1;             % Δe        → 比例增量
x2 = e;                   % e         → 积分项
x3 = e - 2*e_1 + e_2;    % Δ²e       → 微分增量
x  = [x1; x2; x3];       % RBF 输入向量 [3×1]

% --------------------------------------------------------
%  Step 2：RBF 网络前向传播——计算隐层高斯激活 h
% --------------------------------------------------------
h = zeros(n_hidden, 1);
for j = 1:n_hidden
    diff  = x - c(j, :)';                           % [3×1] 输入与中心的差
    h(j)  = exp(-norm(diff)^2 / (2 * b(j)^2 + eps));% 高斯核输出（eps防除零）
end

% 网络输出（更新权重前，用旧权重 w 计算当前预测值）
net_out = w' * h;   % 标量，近似 ∂y/∂u（Jacobian）

% --------------------------------------------------------
%  Step 3：在线权重更新（LMS，辨识误差驱动）
%  原理：网络在 k-1 步用 h(k-1) 预测 y(k)，
%        在 k 步拿到真实 y(k) 后计算辨识误差并更新权重
% --------------------------------------------------------
id_err  = yout - y_hat;             % 辨识误差：实际输出 vs 上步预测
delta_w = eta_w * id_err * h_1;     % LMS 梯度方向
w       = w + delta_w;              % 权重更新

% --------------------------------------------------------
%  Step 4：Jacobian 估计——∂y/∂u ≈ w'·h（输出层加权输出）
%  使用更新后的权重 w 和当前隐层 h
% --------------------------------------------------------
dydU = w' * h;

% 防止 Jacobian 绝对值过小导致梯度消失（保留符号方向）
if abs(dydU) < dydU_min
    dydU = dydU_min * sign(dydU + eps);
end

% --------------------------------------------------------
%  Step 5：梯度下降更新 PID 参数（带动量项）
%  性能指标 E = 0.5·e²
%  链式法则：∂E/∂Kp = ∂E/∂y · ∂y/∂u · ∂u/∂Kp
%           = (−e) · dydU · x1
%  更新方向（负梯度）：Kp += η·e·dydU·x1
%  动量项：α·(Kp − Kp_prev)，加速收敛并抑制振荡
% --------------------------------------------------------
Kp_new = Kp + eta_kp * e * dydU * x1 + momentum * (Kp - Kp_prev);
Ki_new = Ki + eta_ki * e * dydU * x2 + momentum * (Ki - Ki_prev);
Kd_new = Kd + eta_kd * e * dydU * x3 + momentum * (Kd - Kd_prev);

% 参数限幅（防止负增益 / 发散）
Kp_new = max(Kp_min, min(Kp_max, Kp_new));
Ki_new = max(Ki_min, min(Ki_max, Ki_new));
Kd_new = max(Kd_min, min(Kd_max, Kd_new));

% --------------------------------------------------------
%  Step 6：增量式 PID 计算控制量
%  使用本步【更新后】的 Kp/Ki/Kd 生成控制量
% --------------------------------------------------------
delta_u = Kp_new * x1 + Ki_new * x2 + Kd_new * x3;
u       = u_1 + delta_u;
u       = max(u_min, min(u_max, u));   % 执行器限幅

% --------------------------------------------------------
%  Step 7：存储本步预测值（供下步辨识误差计算）
%  ŷ(k+1) = w(k)' · h(k)，在 k+1 步与 yout(k+1) 比较
% --------------------------------------------------------
y_hat_next = w' * h;

% --------------------------------------------------------
%  Step 8：状态变量滚动更新
% --------------------------------------------------------
Kp_prev = Kp;     Ki_prev = Ki;     Kd_prev = Kd;
Kp      = Kp_new; Ki      = Ki_new; Kd      = Kd_new;
e_2     = e_1;    e_1     = e;
u_1     = u;
h_1     = h;
y_hat   = y_hat_next;

% --------------------------------------------------------
%  输出
% --------------------------------------------------------
kp = Kp;
ki = Ki;
kd = Kd;

end
% =========================================================
%  参数调整指南（调试时按此顺序）
% ---------------------------------------------------------
%  1. 系统震荡/发散：
%       先降低 eta_kp / eta_ki / eta_kd（减半逐步试）
%       再检查 u_max/u_min 是否与实际对象匹配
%
%  2. 收敛太慢：
%       适当增大 eta_kp（通常比 eta_ki/eta_kd 大3~5倍）
%       增大 momentum（上限 0.3 左右）
%
%  3. Kp/Ki/Kd 长时间不动（冻结）：
%       dydU_min 设得太大，调小至 1e-5
%       或初始权重 w 过小，增大 w 初始化系数（0.5→1.0）
%
%  4. 辨识不准（id_err 持续很大）：
%       增大 eta_w（上限约 0.5）
%       或增加 n_hidden（6→10）+ 扩大 c_spread
%
%  5. 不同采样周期适配：
%       Ts 变小 → 适当减小 eta_* （变化量更细密）
%       Ts 变大 → 适当增大 eta_*
% =========================================================
